using namespace std;

int number_needed(string a, string b) {
    vector<int> cnt(26, 0);
    for (auto c: a)
        cnt[c-'a']++;
    for (auto c: b)
        cnt[c-'a']--;
    int res = 0;
    for (auto value: cnt)
        res += abs(value);
    return res;
}

int main(){
    string a;
    cin >> a;
    string b;
    cin >> b;
    cout << number_needed(a, b) << endl;
    return 0;
}
